        }

        private DateTime GetRandomDate(int minYear = 1950, int? maxYear = null)
        {
            if (maxYear == null)
            {
                maxYear = DateTime.Now.Year;
            }
            return new DateTime(_rnd.Next(minYear, maxYear.Value), _rnd.Next(1,12), _rnd.Next(1, 28));
        }

        private DateTime GetRandomDate(DateTime minDate, DateTime? maxDate = null)
        {
            if (maxDate == null)
            {
                maxDate = DateTime.Now;
            }
            var maxDays = (int)(maxDate.Value - minDate).TotalDays;
            return minDate.AddDays(_rnd.Next(0,maxDays));
        }

        private T GetAndRemoveRandomItem<T>(List<T> picklist, IEnumerable<T> source)
        {
            if (!picklist.Any())
            {
                picklist.AddRange(source);
            }
            var index = _rnd.Next(0, picklist.Count - 1);

            var name = picklist[index];

            picklist.RemoveAt(index);

            return name;
        }

        private T GetRandomItem<T>(IReadOnlyList<T> source)
        {
            var index = _rnd.Next(0, source.Count - 1);

            return source[index];
        }

