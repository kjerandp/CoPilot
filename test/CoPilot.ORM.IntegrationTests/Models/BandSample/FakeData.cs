            writer.Save<Band>(bands);

            var members = GetBandMembers(bands, persons);
            writer.Save<BandMember>(members);

            var recordings = GetRecordings(bands, genres);
            writer.Save<Recording>(recordings);

            var albums = GetAlbums(recordings);
            writer.Save<Album>(albums, "Tracks");
        }

        private Album[] GetAlbums(Recording[] recordings)
        {
            var bandRecordings =
                recordings.GroupBy(r => r.Band, (band, bandrecordings) => new {Key = band, Value = bandrecordings})
                    .ToDictionary(k => k.Key, v => v.Value);

            var albums = new List<Album>();

            foreach (var bandRecording in bandRecordings)
            {
                var orderedRecordings = bandRecording.Value.OrderBy(r => r.Recorded).ToList();

                while (orderedRecordings.Any())
                {
                    var tracks = _rnd.Next(Math.Min(orderedRecordings.Count, 6), Math.Min(orderedRecordings.Count, 14));
                    var albumrecordings = orderedRecordings.GetRange(0, tracks);
                    orderedRecordings.RemoveRange(0, tracks);
                    var t = 1;
                    var album = new Album
                    {
                        Title = albumrecordings.First().SongTitle,
                        Released = albumrecordings.Max(r => r.Recorded),
                        Tracks = albumrecordings.Select(r => new AlbumTrack
                        {
                            Recording = r,
                            TrackNumber = t++
                        }).ToList()
                    };
                    albums.Add(album);
                }
            }
            return albums.ToArray();
        }

        private Recording[] GetRecordings(Band[] bands, MusicGenre[] genres)
        {
            var recordings = new List<Recording>();
            
            foreach (var band in bands)
            {
                var genre = GetRandomItem(genres);
                var number = _rnd.Next(10, 500);
                var titles = new List<string>();
                for (var i = 0; i < number; i++)
                {
                    var recording = new Recording
                    {
                        Band = band,
                        Genre = genre,
                        Duration = TimeSpan.FromSeconds(_rnd.Next(60,60*7)),
                        Recorded = GetRandomDate(band.Formed),
                        SongTitle = GetAndRemoveRandomItem(titles, _songtitles)
                    };
                    recordings.Add(recording);
                }
            }

            return recordings.ToArray();
        }

        private Person[] GetPersons(City[] cities, int number)
        {
            var personNamesList = new List<string>();
            var persons = new List<Person>();
            for (var i = 0; i < number; i++)
            {
                var pers = new Person
                {
                    Name = GetAndRemoveRandomItem(personNamesList, _persons),
                    City = GetRandomItem(cities),
                    DateOfBirth = GetRandomDate(1920, 2000)
                };
                persons.Add(pers);
            }
            return persons.ToArray();
        }

        private Band[] GetBands(City[] cities, int number)
        {
            var bandNamesList = new List<string>();
            var bands = new List<Band>();
            for (var i = 0; i < number; i++)
            {
                var band = new Band
                {
                    Name = GetAndRemoveRandomItem(bandNamesList, _bandNames),
                    Based = GetRandomItem(cities),
                    Formed = GetRandomDate()
                };
                bands.Add(band);
            }
            return bands.ToArray();
        }

        private BandMember[] GetBandMembers(Band[] bands, Person[] people)
        {
            var peopleList = new List<Person>();
            var members = new List<BandMember>();
            var artistNames = new List<string>();
            var instrumentList = new List<string>();
            foreach (var band in bands)
            {
                
                var number = _rnd.Next(1, 7);
                for (var i = 0; i < number; i++)
                {
                    var left = _rnd.Next(10) > 7
                        ? (DateTime?) GetRandomDate(band.Formed, band.Formed.AddYears(1))
                        : null;

                    var joined = GetRandomDate(band.Formed, left ?? DateTime.Now);
                    var artistName = _rnd.Next(10) > 5 ? GetAndRemoveRandomItem(artistNames, _artistNames) : null;

                    var member = new BandMember
                    {
                        Band = band,
                        Person = GetAndRemoveRandomItem(peopleList, people),
                        Left = left,
                        ArtistName = artistName,
                        Joined = joined,
                        Instrument = GetAndRemoveRandomItem(instrumentList, new[] {"Lead guitar", "Rhythm guitar", "Bass guitar", "Drums", "Keyboard", "Vocals"})
                    };
                    members.Add(member);
                }
            }

            return members.ToArray();
        }

